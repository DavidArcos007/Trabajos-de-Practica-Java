package BuilderVehiculo1;

/**
 *
 * @author marce
 */
public class Carroceria {
    private String material;
    private String tipoCarroceria;
    private boolean avitaculoReforzado;

    public String getMaterial() {
        return material;
    }

    public void setMaterial(String material) {
        this.material = material;
    }

    public String getTipoCarroceria() {
        return tipoCarroceria;
    }

    public void setTipoCarroceria(String tipoCarroceria) {
        this.tipoCarroceria = tipoCarroceria;
    }

    public boolean getAvitaculoReforzado() {
        return avitaculoReforzado;
    }

    public void setAvitaculoReforzado(boolean avitaculoReforzado) {
        this.avitaculoReforzado = avitaculoReforzado;
    }
}
